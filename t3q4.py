''' 
ordered data type:
    ->An ordered data type, also known as a sequential data type, preserves the order of its elements.
    ->Elements within the data structure have a specific position or index assigned to them.
    ->Examples : arrays, lists, tuples, and strings.


undordered data types:
    ->An unordered data type, also known as a non-sequential data type, does not preserve the order of its elements.
    ->Elements within the data structure do not have a specific position or index associated with them.
    ->Examples : sets and dictionaries.
'''